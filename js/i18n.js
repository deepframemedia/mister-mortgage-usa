/* ============================================================
   Core engine
   ============================================================ */
(function () {
  var STORAGE_KEY = 'mm_lang';
  var DEFAULT_LANG = 'en';

  function t(lang, key) {
    return (I18N[lang] && I18N[lang][key]) ? I18N[lang][key] : (I18N[DEFAULT_LANG][key] || key);
  }

  function applyLang(lang) {
    if (!I18N[lang]) lang = DEFAULT_LANG;

    /* Update all [data-i18n] elements */
    document.querySelectorAll('[data-i18n]').forEach(function (el) {
      var key = el.getAttribute('data-i18n');
      var val = t(lang, key);
      if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
        el.placeholder = val;
      } else {
        el.innerHTML = val;
      }
    });

    /* Update [data-i18n-placeholder] */
    document.querySelectorAll('[data-i18n-placeholder]').forEach(function (el) {
      var key = el.getAttribute('data-i18n-placeholder');
      el.placeholder = t(lang, key);
    });

    /* Persist */
    try { localStorage.setItem(STORAGE_KEY, lang); } catch (e) {}

    /* Update toggle button appearance */
    var btn = document.getElementById('lang-toggle');
    if (btn) {
      btn.setAttribute('data-current-lang', lang);
      btn.setAttribute('aria-label', lang === 'es' ? 'Switch to English' : 'Cambiar a Español');
    }

    /* Update <html lang> attribute */
    document.documentElement.lang = lang;

    /* Re-render modal if open so its content updates to new language */
    var modal = document.getElementById('prequal-modal');
    if (modal && modal.classList.contains('open') && typeof renderModalStep === 'function') {
      renderModalStep();
    }
  }

  function toggleLang() {
    var btn = document.getElementById('lang-toggle');
    var current = (btn && btn.getAttribute('data-current-lang')) || getSavedLang();
    applyLang(current === 'en' ? 'es' : 'en');
  }

  function getSavedLang() {
    try { return localStorage.getItem(STORAGE_KEY) || DEFAULT_LANG; } catch (e) { return DEFAULT_LANG; }
  }

  /* Expose globally */
  window.applyLang  = applyLang;
  window.toggleLang = toggleLang;

  /* Auto-init on DOM ready */
  document.addEventListener('DOMContentLoaded', function () {
    applyLang(getSavedLang());
  });
})();
